void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*fin;

	if (lst)
	{
		if (*lst)
		{
			fin = ft_lstlast(*lst);
			fin->next = new;
		}
		else
			*lst = new;
	}
}
